#!/usr/bin/env bash

_help_desc "vim" "编辑器" "Vim 常用操作"

help-vim() {
  cat <<'EOF'
── Vim ──────────────────────────────────────────────────────
一、模式切换

  按键       作用                     说明
  ──────────────────────────────────────────────────────
  i          光标前插入               最常用的进入编辑方式
  a          光标后插入               追加内容时用
  o          下一行新建行插入         快速在当前行下面写新行
  O（大写）  上一行新建行插入         在当前行上面插入
  I（大写）  行首插入                 跳到行头再进编辑
  A（大写）  行尾插入                 跳到行尾再进编辑
  Esc        退回普通模式             任何时候按 Esc 都安全

──────────────────────────────────────────────────────────────
二、选择 → 复制 → 粘贴

  1. 选择文本（可视模式）
  按键       作用                     说明
  ──────────────────────────────────────────────────────
  v          字符级选择               移动光标即可扩展选区
  V（大写）  行级选择                 选整行更高效
  Ctrl+v     块级矩形选择             选表格 / 多列代码超实用

  2. 复制
  按键   作用                         说明
  ──────────────────────────────────────────────────────
  y      复制已选中内容               先选文本再按 y
  yy     复制当前整行                 不需要先选，直接按
  3yy    向下复制 3 行                数字 + yy 批量复制
  y$     复制光标到行尾               精准复制行内片段

  3. 粘贴
  按键       作用                     说明
  ──────────────────────────────────────────────────────
  p          粘贴到光标下方/右侧      最常用
  P（大写）  粘贴到光标上方/左侧      贴到当前位置之前

──────────────────────────────────────────────────────────────
三、撤销 / 重做

  按键       作用                     说明
  ──────────────────────────────────────────────────────
  u          撤销（undo）             反复按可多步撤销
  Ctrl+r     重做（redo）             把撤销的操作再做回来
  .（点号）  重复上一次操作           超实用，省重复劳动

──────────────────────────────────────────────────────────────
四、查找 / 替换

  查找
  按键       作用                     说明
  ──────────────────────────────────────────────────────
  /word      向下查找 word            回车确认，高亮匹配
  ?word      向上查找 word            方向相反
  n          跳到下一个匹配           配合 / 或 ? 使用
  N（大写）  跳到上一个匹配           反向跳转

  替换（命令模式）
  命令                      作用
  ──────────────────────────────────────────────────────
  :%s/old/new/g             全文替换，不确认
  :%s/old/new/gc            全文替换，逐个确认（y=替换 n=跳过）
  :10,20s/old/new/g         只替换第 10~20 行

──────────────────────────────────────────────────────────────
五、相对行号操作（relativenumber）

  左侧显示的是距离当前行的行数，看到几就按几，不用心算：
  按键       作用
  ──────────────────────────────────────────────────────
  3j         向下移动 3 行
  5k         向上移动 5 行
  5dd        删除当前行及下方 4 行（共 5 行）
  4yy        复制当前行及下方 3 行（共 4 行）
  d3k        删除当前行到上方 3 行
  y4j        复制当前行到下方 4 行
  c2j        修改当前行到下方 2 行（删除并进入插入模式）
  >5j        将下方 5 行右缩进
  <3j        将下方 3 行左缩进
  V6j        可视选中当前行到下方 6 行

─────────────────────────────────────────────────────────────
EOF
}
