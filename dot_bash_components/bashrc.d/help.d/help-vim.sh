#!/usr/bin/env bash

_help_desc "vim" "编辑器" "Vim 常用操作"

help-vim() {
  cat <<'EOF'
── Vim ──────────────────────────────────────────────────────
一、模式切换

  按键       作用                     说明
  ──────────────────────────────────────────────────────
  i          光标前插入               最常用的进入编辑方式
  a          光标后插入               追加内容时用
  o          下一行新建行插入         快速在当前行下面写新行
  O（大写）  上一行新建行插入         在当前行上面插入
  I（大写）  行首插入                 跳到行头再进编辑
  A（大写）  行尾插入                 跳到行尾再进编辑
  Esc        退回普通模式             任何时候按 Esc 都安全

──────────────────────────────────────────────────────────────
二、选择 → 复制 → 粘贴

  1. 选择文本（可视模式）
  按键       作用                     说明
  ──────────────────────────────────────────────────────
  v          字符级选择               移动光标即可扩展选区
  V（大写）  行级选择                 选整行更高效
  Ctrl+v     块级矩形选择             选表格 / 多列代码超实用

  2. 复制
  按键   作用                         说明
  ──────────────────────────────────────────────────────
  y      复制已选中内容               先选文本再按 y
  yy     复制当前整行                 不需要先选，直接按
  3yy    向下复制 3 行                数字 + yy 批量复制
  y$     复制光标到行尾               精准复制行内片段

  3. 粘贴
  按键       作用                     说明
  ──────────────────────────────────────────────────────
  p          粘贴到光标下方/右侧      最常用
  P（大写）  粘贴到光标上方/左侧      贴到当前位置之前

──────────────────────────────────────────────────────────────
三、撤销 / 重做

  按键       作用                     说明
  ──────────────────────────────────────────────────────
  u          撤销（undo）             反复按可多步撤销
  Ctrl+r     重做（redo）             把撤销的操作再做回来
  .（点号）  重复上一次操作           超实用，省重复劳动

──────────────────────────────────────────────────────────────
四、查找 / 替换

  查找
  按键       作用                     说明
  ──────────────────────────────────────────────────────
  /word      向下查找 word            回车确认，高亮匹配
  ?word      向上查找 word            方向相反
  n          跳到下一个匹配           配合 / 或 ? 使用
  N（大写）  跳到上一个匹配           反向跳转

  替换（命令模式）
  命令                      作用
  ──────────────────────────────────────────────────────
  :%s/old/new/g             全文替换，不确认
  :%s/old/new/gc            全文替换，逐个确认（y=替换 n=跳过）
  :10,20s/old/new/g         只替换第 10~20 行

──────────────────────────────────────────────────────────────
五、相对行号操作（relativenumber）

  左侧显示的是距离当前行的行数，看到几就按几，不用心算：
  按键       作用
  ──────────────────────────────────────────────────────
  3j         向下移动 3 行
  5k         向上移动 5 行
  5dd        删除当前行及下方 4 行（共 5 行）
  4yy        复制当前行及下方 3 行（共 4 行）
  d3k        删除当前行到上方 3 行
  y4j        复制当前行到下方 4 行
  c2j        修改当前行到下方 2 行（删除并进入插入模式）
  >5j        将下方 5 行右缩进
  <3j        将下方 3 行左缩进
  V6j        可视选中当前行到下方 6 行

──────────────────────────────────────────────────────────────
六、注释（tcomment 插件）

  按键       作用                     说明
  ──────────────────────────────────────────────────────
  gcc        注释/取消注释当前行       再按一次即取消注释
  gc（可视） 注释/取消注释选中区域     V 选中多行后按 gc
  gc3j       注释当前行及下方 3 行     配合相对行号使用
  gcip       注释当前段落             ip = inner paragraph

  自动识别文件类型注释符：
    .sh  → #    .c/.js → //    .py → #    .vim → "

──────────────────────────────────────────────────────────────
七、光标快速移动

  按键       作用                     说明
  ──────────────────────────────────────────────────────
  gg         跳到文件第一行
  G          跳到文件最后一行
  5G         跳到第 5 行              nG 跳任意行
  H          跳到屏幕第一行           屏顶 (High)
  M          跳到屏幕中间行           屏中 (Middle)
  L          跳到屏幕最后一行         屏底 (Low)
  w          跳到下一个词首
  b          跳到上一个词首
  e          跳到下一个词尾
  W / B / E  同 w/b/e，空格分隔       忽略标点
  0          跳到行首第一列
  ^          跳到行首第一个非空字符
  $          跳到行末
  f<字符>    跳到行内下一个 <字符>     ; 重复、, 反向
  F<字符>    跳到行内上一个 <字符>
  t<字符>    跳到行内下一个 <字符> 前  T 反向
  {          跳到上一空行              段落上移
  }          跳到下一空行              段落下移
  Ctrl-u     半页上翻
  Ctrl-d     半页下翻
  Ctrl-b     整页上翻
  Ctrl-f     整页下翻
  Ctrl-o     跳回上一个位置            配合 gg/G 用
  Ctrl-i     跳到下一个位置
  %          跳到匹配的括号            () {} []

──────────────────────────────────────────────────────────────
八、删除

  按键       作用                     说明
  ──────────────────────────────────────────────────────
  x          删除光标下字符
  X          删除光标前字符
  dd         删除整行
  3dd        删除 3 行                ndd 删 n 行
  dw         删除到词尾
  de         删除到词尾（不含空格）
  db         删除到词首
  d$ / D     删除到行尾
  d^         删除到行首
  dG         删除到文件末尾
  dgg        删除到文件开头
  diw        删除当前词               iw = inner word
  di"        删除双引号内内容          也可 di' di( di[

──────────────────────────────────────────────────────────────
九、文件与窗口

  按键 / 命令            作用                     说明
  ──────────────────────────────────────────────────────
  :w                     保存
  :q                     退出
  :wq / ZZ               保存并退出
  :q! / ZQ               不保存强制退出
  :e <file>              打开文件
  :bn                    下一个 buffer
  :bp                    上一个 buffer
  :sp <file>             水平分屏打开
  :vs <file>             垂直分屏打开
  Ctrl+w h/j/k/l         窗口间移动
  Ctrl+w s               水平分屏（当前文件）
  Ctrl+w v               垂直分屏（当前文件）
  Ctrl+w q               关闭当前窗口
  Ctrl+w =               窗口等宽
─────────────────────────────────────────────────────────────
EOF
}
