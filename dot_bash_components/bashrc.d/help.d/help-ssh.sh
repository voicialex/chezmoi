  cat <<'EOF'
── SSH ──────────────────────────────────────────────────────
  复制公钥到远端:
    ssh-copy-id jetson@192.168.2.100

  清除旧 host key（IP 变了时用）:
    ssh-keygen -R 192.168.2.99
─────────────────────────────────────────────────────────────
EOF
}
